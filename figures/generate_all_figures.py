# Figure S3 – Hyperparameter search results (Supplementary S2.2)
# ════════════════════════════════════════════════════════════════════════════

def generate_figure_s3():
    """
    Panel A: Validation AUROC vs. Optuna trial index (convergence plot).
    Panel B: Bar chart of top-10 configurations (Table S2.1).
    """
    rng = np.random.default_rng(42)

    # Simulate 50 trial AUROC values converging to 0.939
    n_trials = 50
    base_aurocs = np.clip(
        rng.normal(0.910, 0.015, n_trials), 0.870, 0.945)
    # Best at trial 1 (rank 1 config, 0-indexed)
    trial_aurocs = np.sort(base_aurocs)[::-1]
    trial_aurocs = trial_aurocs[rng.permutation(n_trials)]
    trial_aurocs[0] = 0.939  # trial 1 is best per paper

    best_so_far = np.maximum.accumulate(trial_aurocs)

    # Top-10 from Table S2.1
    top10_auroc = [0.939, 0.937, 0.937, 0.936, 0.936,
                   0.935, 0.934, 0.931, 0.921, 0.918]
    top10_labels = [f'Rank {i+1}' for i in range(10)]

    fig, axes = plt.subplots(1, 2, figsize=(7.2, 3.2))
    fig.subplots_adjust(wspace=0.42)

    # Panel A: convergence
    ax = axes[0]
    ax.scatter(range(1, n_trials + 1), trial_aurocs,
               color=C['gray'], s=15, alpha=0.5, label='Trial AUROC')
    ax.plot(range(1, n_trials + 1), best_so_far,
            color=C['red'], lw=1.5, label='Best so far')
    ax.axhline(0.939, color=C['blue'], lw=0.9, linestyle='--', label='Best = 0.939')
    ax.set_xlabel('Optuna trial index')
    ax.set_ylabel('Validation AUROC (5-fold CV)')
    ax.set_title('Bayesian search convergence\n(50 trials, TPE sampler)', pad=6)
    ax.legend(fontsize=7)
    ax.set_ylim(0.86, 0.95)
    panel_label(ax, 'A')

    # Panel B: top-10 bar
    ax2 = axes[1]
    colors_bar = [C['red'] if i == 0 else C['blue'] for i in range(10)]
    bars = ax2.barh(top10_labels[::-1], top10_auroc[::-1],
                    color=colors_bar[::-1], height=0.6, edgecolor='white')
    ax2.set_xlabel('Validation AUROC')
    ax2.set_title('Top-10 configurations\n(Table S2.1)', pad=6)
    ax2.set_xlim(0.90, 0.945)
    for bar, val in zip(bars, top10_auroc[::-1]):
        ax2.text(val + 0.0003, bar.get_y() + bar.get_height() / 2,
                 f'{val:.3f}', va='center', ha='left', fontsize=7.5)
    # Star on rank 1
    ax2.text(0.944, 9, '★ Best (d=256, L=2, h=4)', va='center',
             ha='right', fontsize=7, color=C['red'])
    panel_label(ax2, 'B')

    path = os.path.join(OUTDIR, 'Figure_S3_hyperparameter_search.tiff')
    fig.savefig(path, bbox_inches='tight')
    plt.close(fig)
    print(f'  ✓ Saved {path}')


