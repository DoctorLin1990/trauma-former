# Figure S4 – Alert persistence sensitivity analysis (Section 3.4)
# ════════════════════════════════════════════════════════════════════════════

def generate_figure_s4():
    """
    Trade-off between alert persistence requirement, early warning time (EWT),
    and false-positive rate (FPR).
    Paper values: 1 min → EWT 21.4 min, FPR 15.1%;
                  3 min → EWT 18.1 min, FPR 8.3%;
                  5 min → EWT 14.6 min, FPR 5.9%
    """
    persistence = [1, 2, 3, 4, 5]
    ewt   = [21.4, 19.8, 18.1, 16.2, 14.6]
    fpr   = [15.1, 11.2,  8.3,  6.9,  5.9]
    ppv25 = [0.38, 0.42,  0.46, 0.47, 0.49]

    fig, axes = plt.subplots(1, 2, figsize=(7.2, 3.2))
    fig.subplots_adjust(wspace=0.42)

    ax = axes[0]
    color_ewt = C['blue']
    color_fpr = C['red']
    ax2_twin  = ax.twinx()
    l1, = ax.plot(persistence, ewt, color=color_ewt, lw=1.8, marker='o',
                  ms=5, label='Early warning time (min)')
    l2, = ax2_twin.plot(persistence, fpr, color=color_fpr, lw=1.8,
                        marker='s', ms=5, linestyle='--', label='FPR (%)')
    ax.axvline(3, color=C['gray'], lw=0.9, linestyle=':', alpha=0.8)
    ax.text(3.05, ewt[2] + 0.3, '3-min\ncompromise', fontsize=7,
            color=C['gray'], va='bottom')
    ax.set_xlabel('Alert persistence requirement (min)')
    ax.set_ylabel('Early warning time (min)', color=color_ewt)
    ax2_twin.set_ylabel('False-positive rate (%)', color=color_fpr)
    ax2_twin.spines['right'].set_visible(True)
    ax2_twin.spines['right'].set_linewidth(0.8)
    ax.set_title('EWT vs. FPR trade-off\n(development cohort)', pad=6)
    lines = [l1, l2]
    ax.legend(lines, [l.get_label() for l in lines], fontsize=7, loc='center right')
    panel_label(ax, 'A')

    # Panel B: PPV at 25% prevalence vs persistence
    ax3 = axes[1]
    ax3.plot(persistence, ppv25, color=C['purple'], lw=1.8, marker='^',
             ms=5, label='PPV (25% prevalence)')
    ax3.axhline(0.5, color=C['gray'], lw=0.8, linestyle=':', label='PPV = 0.50')
    ax3.axvline(3, color=C['gray'], lw=0.9, linestyle=':', alpha=0.8)
    ax3.set_xlabel('Alert persistence requirement (min)')
    ax3.set_ylabel('PPV (alert-rule, 25% prevalence)')
    ax3.set_title('PPV vs. persistence\n(independent test set, 25% prevalence)', pad=6)
    ax3.set_ylim(0.3, 0.6)
    ax3.legend(fontsize=7)
    panel_label(ax3, 'B')

    path = os.path.join(OUTDIR, 'Figure_S4_alert_persistence.tiff')
    fig.savefig(path, bbox_inches='tight')
    plt.close(fig)
    print(f'  ✓ Saved {path}')


