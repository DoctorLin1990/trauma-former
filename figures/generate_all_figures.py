# Figure S6 – Non-linear stress test results (Supplementary S3 / Table S3.2)
# ════════════════════════════════════════════════════════════════════════════

def generate_figure_s6():
    """
    Panel A: AUROC comparison between linear (primary) and non-linear datasets.
    Panel B: Brier score comparison (model calibration degradation).
    Source: Supplementary Table S3.2.
    """
    models    = ['Trauma-Former', '1D-CNN', 'GRU']
    auroc_lin = [0.939, 0.868, 0.854]
    auroc_nl  = [0.815, 0.790, 0.703]
    brier_lin = [0.110, 0.160, 0.170]
    brier_nl  = [0.190, 0.220, 0.240]

    # Try loading results from run_stress_test output
    stress_path = os.path.join(_ROOT, 'results', 'S3_nonlinear_results.csv')
    if os.path.exists(stress_path):
        import csv
        with open(stress_path) as f:
            reader = csv.DictReader(f)
            for i, row in enumerate(reader):
                if i < len(models):
                    try:
                        auroc_nl[i]  = float(row.get('auroc', auroc_nl[i]))
                        brier_nl[i]  = float(row.get('brier', brier_nl[i]))
                    except (ValueError, KeyError):
                        pass

    x  = np.arange(len(models))
    w  = 0.35

    fig, axes = plt.subplots(1, 2, figsize=(7.2, 3.4))
    fig.subplots_adjust(wspace=0.42)

    # Panel A: AUROC
    ax = axes[0]
    b1 = ax.bar(x - w / 2, auroc_lin, w, label='Linear (primary)', color=C['blue'],
                edgecolor='white', linewidth=0.6)
    b2 = ax.bar(x + w / 2, auroc_nl,  w, label='Non-linear (stress)', color=C['red'],
                edgecolor='white', linewidth=0.6, alpha=0.85)
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=15, ha='right')
    ax.set_ylabel('AUROC')
    ax.set_ylim(0.65, 0.96)
    ax.axhline(0.9, color=C['gray'], lw=0.7, linestyle=':', label='AUROC = 0.90')
    ax.set_title('AUROC: Linear vs. Non-linear\n(Supplementary Table S3.2)', pad=6)
    ax.legend(fontsize=7)
    for b, v in zip(b1, auroc_lin):
        ax.text(b.get_x() + b.get_width() / 2, v + 0.002,
                f'{v:.3f}', ha='center', va='bottom', fontsize=7)
    for b, v in zip(b2, auroc_nl):
        ax.text(b.get_x() + b.get_width() / 2, v + 0.002,
                f'{v:.3f}', ha='center', va='bottom', fontsize=7)
    # Draw ΔAUROC arrows
    for i in range(len(models)):
        delta = auroc_lin[i] - auroc_nl[i]
        ax.annotate('', xy=(x[i] + w / 2, auroc_nl[i]),
                    xytext=(x[i] - w / 2, auroc_lin[i]),
                    arrowprops=dict(arrowstyle='->', color=C['purple'], lw=0.8))
        ax.text(x[i] + 0.02, (auroc_lin[i] + auroc_nl[i]) / 2 + 0.005,
                f'Δ={delta:.3f}', fontsize=6.5, color=C['purple'])
    panel_label(ax, 'A')

    # Panel B: Brier score
    ax2 = axes[1]
    b3 = ax2.bar(x - w / 2, brier_lin, w, label='Linear (primary)', color=C['blue'],
                 edgecolor='white', linewidth=0.6)
    b4 = ax2.bar(x + w / 2, brier_nl, w, label='Non-linear (stress)', color=C['red'],
                 edgecolor='white', linewidth=0.6, alpha=0.85)
    ax2.set_xticks(x)
    ax2.set_xticklabels(models, rotation=15, ha='right')
    ax2.set_ylabel('Brier Score (↓ better)')
    ax2.set_title('Calibration: Linear vs. Non-linear\n(Brier Score)', pad=6)
    ax2.legend(fontsize=7)
    for b, v in zip(b3, brier_lin):
        ax2.text(b.get_x() + b.get_width() / 2, v + 0.002,
                 f'{v:.3f}', ha='center', va='bottom', fontsize=7)
    for b, v in zip(b4, brier_nl):
        ax2.text(b.get_x() + b.get_width() / 2, v + 0.002,
                 f'{v:.3f}', ha='center', va='bottom', fontsize=7)
    panel_label(ax2, 'B')

    fig.text(0.5, -0.04,
             '⚠ Non-linear results: post-hoc, single 80/20 split, confounded (Supplementary S3.6). Not directly comparable to primary analysis.',
             ha='center', fontsize=6.5, color=C['red'], style='italic',
             wrap=True)

    path = os.path.join(OUTDIR, 'Figure_S6_nonlinear_stress.tiff')
    fig.savefig(path, bbox_inches='tight')
    plt.close(fig)
    print(f'  ✓ Saved {path}')


